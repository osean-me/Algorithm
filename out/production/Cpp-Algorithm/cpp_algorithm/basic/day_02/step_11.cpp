#include <bits/stdc++.h>

using namespace std;

int main() {
  // long long 타입은 8 byte 크기의 정수 타입이다.
  long long ll = -1e9;
  cout << ll << "\n";

  // unsigned long long 타입은 8 byte 크기의 양의 정수 타입이다.
  unsigned long long ull = 1e9;
  cout << ull << "\n";

  return 0;
}
